function square() {
    let input = prompt('give me a number');
    let result = 0;

    result = Number(input) * Number(input);

    alert(result);
}

function multiplier() {
    let number1 = prompt('Give me the first number');
    let number2 = prompt('Give me a second number');
    let result = 0;

    result = Number(number1) * Number(number2);

    alert(result);
}
